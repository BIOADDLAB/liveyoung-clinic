-- 8. 회원탈퇴용 DB 함수 (auth.users 삭제 → profiles cascade 자동 삭제)
create or replace function delete_user()
returns void
language plpgsql
security definer
as $$
begin
  delete from auth.users where id = auth.uid();
end;
$$;

